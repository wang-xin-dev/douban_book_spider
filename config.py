"""
配置文件 —— 豆瓣高分图书爬虫项目
===================================
本文件集中管理项目中的所有可调参数，方便后续修改迭代。
修改这里的值即可全局生效，无需深入到各个模块中查找硬编码。

模块分工：
    config.py     - 本文件，所有可配置参数
    spider.py     - 爬虫模块（读取爬虫配置）
    cleaner.py    - 数据清洗模块（读取清洗配置）
    visualizer.py - 可视化模块（读取图表配置）
    main.py       - 主程序入口（读取输出文件配置）

使用建议：
    1. 首次使用无需修改任何参数
    2. 想爬更多页 → 改 CRAWL_PAGES
    3. 想换目标榜单 → 改 BASE_URL
    4. 被封了 → 增大 REQUEST_DELAY
"""

# ============================================================
# 一、爬虫配置
# ============================================================

# 目标URL：豆瓣「高分图书」公开豆列（无需登录即可访问）
# 这是豆瓣用户整理的公开高分图书榜单，非豆瓣官方页面
BASE_URL = "https://www.douban.com/doulist/1264675/"

# 抓取页数：每页约25本书，3页共约75本
# 豆瓣豆列默认每页显示25条，通过 start 参数翻页
# 想抓更多页直接改这个数字即可（建议不超过5页，减少服务器压力）
CRAWL_PAGES = 3

# 请求头：模拟 Chrome 浏览器发送请求
# 作用：让服务器认为我们是正常用户在浏览网页，而不是爬虫脚本
# 注意：不包含 Cookie、Token 等任何隐私信息，代码可直接开源
HEADERS = {
    # User-Agent：告诉服务器我们是什么浏览器
    "User-Agent": (
        "Mozilla/5.0 (Windows NT 10.0; Win64; x64) "
        "AppleWebKit/537.36 (KHTML, like Gecko) "
        "Chrome/120.0.0.0 Safari/537.36"
    ),
    # Accept：告诉服务器我们能接收什么格式的响应
    "Accept": (
        "text/html,"
        "application/xhtml+xml,"
        "application/xml;q=0.9,"
        "*/*;q=0.8"
    ),
    # Accept-Language：告诉服务器我们偏好什么语言
    "Accept-Language": "zh-CN,zh;q=0.9,en;q=0.8",
    # 注意：不设置 Accept-Encoding，让 requests 库自动处理压缩/解压
    # 如果手动指定 "gzip, deflate, br"，requests 可能无法正确解压 br 格式
    # 导致 HTML 乱码、BeautifulSoup 解析不到任何内容
    # Connection：告诉服务器保持连接（减少TCP握手开销）
    "Connection": "keep-alive",
}

# 请求超时时间（秒）
# 超过这个时间服务器还没响应，就认为请求失败
# 豆瓣服务器通常响应较快，15秒是比较保险的值
REQUEST_TIMEOUT = 15

# 每次请求之间的等待时间（秒）
# ★ 爬虫礼仪核心：高频访问会给服务器造成压力，也容易被封IP
# 1.5秒意味着每分钟约40次请求，对服务器影响很小
REQUEST_DELAY = 1.5

# 单次请求失败后的最大重试次数
# 比如网络波动导致超时，会自动重试，最多试3次
MAX_RETRIES = 3

# ============================================================
# 二、输出文件配置
# ============================================================

# Excel 导出文件名
# 程序运行后在项目根目录生成此文件
OUTPUT_EXCEL = "book_data.xlsx"

# 评分分布图保存的文件名
# 这个图适合放在 GitHub README.md 中作为项目配图
OUTPUT_CHART = "score_dist.png"

# ============================================================
# 三、数据清洗配置
# ============================================================

# 空值填充：当某个字段没有数据时，用这个值填充
# 比如某本书没有简介，就填"无"
NULL_FILL_VALUE = "无"

# 有效评分范围（豆瓣评分是10分制）
# 超出这个范围的评分被认为是脏数据，会被过滤掉
SCORE_MIN = 0.0    # 最低有效评分
SCORE_MAX = 10.0   # 最高有效评分

# 有效评价人数范围
# 0 ~ 1000万是合理范围，超出说明数据异常
RATING_COUNT_MIN = 0
RATING_COUNT_MAX = 10_000_000  # Python 支持用下划线分隔大数字，提高可读性

# ============================================================
# 四、可视化配置
# ============================================================

# 图表主标题
CHART_TITLE = "豆瓣高分图书评分分布"

# 图表尺寸（宽, 高），单位：英寸
# 12×6 是比较适合 README 展示的宽幅比例
CHART_FIGSIZE = (12, 6)

# 柱状图颜色（Material Design 绿色系）
# 想换颜色？直接改这个十六进制色值即可
CHART_COLOR = "#4CAF50"

# 图片清晰度 DPI（Dots Per Inch，每英寸像素数）
# 150 是清晰度和文件大小的平衡点
# 想要更清晰可以调到 300（文件会变大）
CHART_DPI = 150
